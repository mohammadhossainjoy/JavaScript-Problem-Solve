/***

Subtask-1:

Find all the odd numbers from 61 to 100.

 */
console.log("Odd Number are: ");

for (let num = 61; num <= 100; num++) {
  if (num % 2 == !0) {
    console.log(num);
  }
}
console.log("Even Number are :");
/*
Subtask-2:

Find all the even numbers from 78 to 98.

 */

for (let numbers = 78; numbers <= 98; numbers++) {
  if (numbers % 2 == 0) {
    console.log(numbers);
  }
}
